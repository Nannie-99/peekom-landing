  const LANG_STORAGE_KEY = "ppaekkom-lang";

  function resolveLang(code) {
    if (!code) return "ko";
    if (STRINGS[code]) return code;
    const prefix = code.split("-")[0];
    const match = Object.keys(STRINGS).find((k) => k === prefix || k.startsWith(prefix + "-"));
    return match || "ko";
  }

  function getCurrentLang() {
    const saved = (typeof localStorage !== "undefined") ? localStorage.getItem(LANG_STORAGE_KEY) : null;
    if (saved && STRINGS[saved]) return saved;
    if (typeof navigator !== "undefined") {
      for (const nav of (navigator.languages || [navigator.language])) {
        const resolved = resolveLang(nav);
        if (resolved) return resolved;
      }
    }
    return "ko";
  }

  function setLang(code) {
    const resolved = resolveLang(code);
    if (typeof localStorage !== "undefined") {
      localStorage.setItem(LANG_STORAGE_KEY, resolved);
    }
    return resolved;
  }

