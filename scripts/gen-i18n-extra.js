const fs = require("fs");
const path = require("path");

const i18nPath = path.join(__dirname, "..", "assets", "i18n.js");
const outPath = path.join(__dirname, "..", "assets", "i18n-locale-extra.js");
const patchDir = path.join(__dirname, "patches");

const src = fs.readFileSync(i18nPath, "utf8");
const m = src.match(/const STRINGS = (\{[\s\S]*?\n  \});\s*\n\s*\/\/ Fill missing/);
if (!m) throw new Error("STRINGS parse failed");
const STRINGS = eval("(" + m[1] + ")");

const LANGS = ["ja", "zh-CN", "zh-TW", "es", "fr", "de", "pt", "it", "ru", "vi", "th", "id", "hi", "ar"];
const keys = Object.keys(STRINGS.en);

function loadPatch(lang) {
  const file = path.join(patchDir, lang + ".json");
  if (!fs.existsSync(file)) return {};
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

const lines = [
  "// Auto-generated by scripts/gen-i18n-extra.js",
  "(function (global) {",
  "  const S = global.PeekomI18n && global.PeekomI18n.STRINGS;",
  "  if (!S) return;"
];

for (const lang of LANGS) {
  const patch = loadPatch(lang);
  const obj = {};
  for (const key of keys) {
    if (lang !== "ko" && (key === "settingsTitlePlusKo" || key === "settingsTitleFreeKo")) {
      obj[key] = patch[key] || STRINGS.en[key === "settingsTitlePlusKo" ? "settingsTitlePlus" : "settingsTitleFree"];
      continue;
    }
    if (patch[key] !== undefined) {
      obj[key] = patch[key];
    } else if (STRINGS[lang] && STRINGS[lang][key] !== undefined) {
      obj[key] = STRINGS[lang][key];
    } else {
      obj[key] = STRINGS.en[key];
    }
  }
  lines.push(`  Object.assign(S[${JSON.stringify(lang)}], ${JSON.stringify(obj)});`);
}

lines.push("})(typeof window !== 'undefined' ? window : global);");
lines.push("");
fs.writeFileSync(outPath, lines.join("\n"), "utf8");
console.log("Wrote", outPath);
